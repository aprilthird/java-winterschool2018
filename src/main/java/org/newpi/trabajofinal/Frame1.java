    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String  num= txtPatientNumber.getText();
        String nom=txtPatientName.getText();
        String surna=txtPatientSurname.getText();
        String dir = txtPatientAddress.getText();
        String codser= txtPatientCodeService.getText();
        String telf= txtPatientNumber.getText();
        String room=txtPatientRoom.getText();
        String Mut=txtPatientMut.getText();
        String cama=txtPatientBed.getText();
        String cmd,cmd2,mensaje;
        
        if(!modificado){
                cmd=String.format(
                "insert into malade(numero,nom,prenom,adresse,tel,mutuelle) "
                +"values('%s','%s','%s','%s','%s','%s');"
                ,num,nom,surna,dir,codser,telf,room,Mut);
                mensaje="Paciente Agregado con exito";
               cmd2=String.format(
                "insert into hospitalisation(no_malade,code_service,no_chambre,lit) values ("
                +"'%s','%s','%s','%s' )"
                ,num,codser,room,cama);
                mensaje="Paciente Agregado con exito";        
                
        }
        else
        {
           cmd=String.format(
                "update malade set"
                +" numero='%s',nom='%s',prenom='%s' "+
                "where "+
                "numero='%s' "
                ,num,nom,surna,oldnum);
           
           cmd2=String.format(
                "update hospitalisation set "
                +"no_malade=%s,code_service='%s',no_chambre='%s',lit='%s' where "
                        + "no_malade=%s"
                ,num,codser,room,cama,oldnum);
           mensaje="Datos del paciente modificados correctamente";
        }
        try {
            new Conexion().exec(cmd);
            new Conexion().exec(cmd2);
          JOptionPane.showMessageDialog(this, mensaje);
        tbp.filter(txtNumber.getText(), txtName.getText(), txtService.getText());
        tbPatients.repaint();
        tbPatients.clearSelection();
        dialogAgregarPaciente.setVisible(false);
        } catch (ClassNotFoundException|SQLException ex) {
               JOptionPane.showMessageDialog(this, ex.getMessage(), "Ocurrió un problema", JOptionPane.ERROR_MESSAGE);
               ex.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed
