    private void btnNurseReport1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNurseReport1ActionPerformed
        DefaultPieDataset dataset = new DefaultPieDataset();
        try {
            List<Object[]> result = new Conexion().nurseReport1();
            result.stream().forEach(o -> { dataset.setValue((String)o[1], (int)o[0]); });
        }
        catch(ClassNotFoundException | SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Ocurrió un error", JOptionPane.ERROR_MESSAGE);
        }
        JFreeChart chart = ChartFactory.createPieChart("Enfermeras por turno", dataset);
        JFrame frame = new JFrame("Enfermeras por Turno");
        frame.setLayout(new BorderLayout());
        frame.add("North", new ChartPanel(chart));
        JButton btn = new JButton("Guardar como Imagen");
        btn.addActionListener(new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                try {
                    File file = new File("reports/Reporte de Enfermeras por Turno.png");
                    ChartUtils.saveChartAsPNG(file, chart, IMAGE_WIDTH, IMAGE_HEIGHT);
                    JOptionPane.showMessageDialog(frame, "Se guardó la imagen en: " + file.getAbsolutePath());
                } catch (IOException ex) {
                    JOptionPane.showMessageDialog(frame, ex.getMessage(), "Ocurrió un error", JOptionPane.ERROR_MESSAGE);
                }
            }
        });
        frame.add("South", btn);

        frame.setVisible(true);
        frame.pack();
    }//GEN-LAST:event_btnNurseReport1ActionPerformed

